6.4. Vắc-xin Paracox
Định nghĩa: Là vắc-xin sống chứa các chủng cầu trùng đã được làm yếu, được tiêm để phòng ngừa bệnh coccidiosis ở gà.
Công dụng: Vắc-xin này được tiêm vào gà con hoặc gà trưởng thành, giúp bảo vệ gà khỏi các loại cầu trùng gây bệnh trong suốt quá trình phát triển.
6.5. Vắc-xin Coxoid
