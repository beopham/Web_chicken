Gà xù lông, khó khăn trong việc đi lại, gà mắc bệnh mệt mỏi, ốm yếu, tuy nhiên giai đoạn này bệnh tiến triển không quá nhanh. Gà mắc bệnh cầu trùng ở thể mãn tính dẫn tới niêm mạc ruột hư hại nặng do đó khó khăn trong việc trao đổi dinh dưỡng, hấp thu thức ăn dẫn tới còi cọc, chậm lớn và tăng cân chậm.
3.3 Thể mang trùng
Thể mang trùng hay còn gọi là thể ẩn bệnh là một dạng khá phức tạp phần lớn gặp ở gà đã trưởng thành và đang sinh đẻ. Ở thể này gà bị mắc bệnh cầu trùng vẫn khoẻ mạnh, ăn uống bình thường, không đi ngoài tiêu chảy hoặc rất ít. 
Tuy nhiên ở thể mang trùng tác hại to lớn nhất chính là tỷ lệ đẻ trứng giảm tới 15 - 20%, đó là lý do mà người chăn nuôi đôi khi cũng không thể tìm ra được những nguyên nhân gây bệnh. 
Bệnh tích của bệnh cầu trùng
Bệnh cầu trùng ở gà nói riêng và ở gia cầm nói chung có bệnh tích vô cùng điển hình, trong đó rõ rệt nhất chính là ở ruột non và manh tràng. 
3.4 Ruột non
Ruột non của gà bị bệnh cầu trùng thường sưng to, nhất là ở đoạn tá tràng, ngoài ra thành ruột dày cộm lên có những chấm trắng. Đặc biệt ruột gà bị trương to, vách ruột dễ vỡ, trong ruột chứa chất lỏng có mùi vô cùng khó chịu. 
Quan sát bề mặt niêm mạc trong ruột xuất hiện nhiều điểm trắng đỏ, manh tràng, tá tràng có màu đỏ sẫm.
3.5 Manh tràng
Manh tràng gà bị bệnh thường sưng rất to, bên trong có xuất huyết và đầy máu, nếu như gà bị mắc bệnh nặng gà sẽ bị xuất huyết manh tràng dẫn tới hoại tử từng mảng đen.
4.Cách phòng bệnh cầu trùng gà?
Áp dụng các biện pháp phòng chống bệnh cấu trùng ở gà là cách để gà không mắc phải bệnh cầu trùng. Bà con cần nắm bắt một số biện pháp đó là:Từ khi là con giống thì bà con nên chọn con giống khoẻ mạnh, không có mầm bệnh. Khi mang con giống về nuôi nên tiêm phòng vaccine để hạn chế bệnh tật. 
Bà con nên mua thức ăn đảm bảo chất lượng và bảo quản thức kỹ lưỡng, tránh bị ẩm mốc. 
Thường xuyên vệ sinh máng ăn, máng uống nước sạch sẽ để không dính mầm bệnh cầu trùng gà. 
Bổ sung thêm các loại men tiêu hoá, vitamin,… để tăng sức đề kháng cho gà. 
Cần để chuồng trại thông thoáng, không để chuồng quá nóng hoặc quá lạnh. Thường xuyên thay lớp độn chuồng mới. 
Xây dựng chuồng trại khang trang, tránh để chuột, rắn, chim chóc lại gần dễ khiến gà mắc bệnh. 
Cần vệ sinh, dọn dẹp chuồng trại sạch sẽ bằng men vi sinh chăn nuôi. Đây là dòng sản phẩm có chứa các vi sinh vật có lợi rất tốt trong việc khử khuẩn, làm sạch môi trường chăn nuôi, rất tốt với vật nuôi và an toàn cho người, môi trường xung quanh. 
5. Cách điều trị bệnh cầu trùng ở gà?
Sau khi đã nắm bắt được nguyên nhân và dấu hiệu bệnh thì bà con cần tìm ra cách chữa trị bệnh cấu trùng gà. Đó là: 
5.1 Sử dụng thuốc thú y chữa bệnh cầu trùng gà
Khi gà mắc bệnh thì bà con có thể sử dụng một số loại thuốc bán ở quầy thú ý như: toltrazuril, diclazuril, amprolium, tetracylin,… Bà con cần tuân thủ hướng dẫn của nhà sản xuất về cách dùng, thời gian sử dụng. Và trong quá trình sử dụng cần lưu ý một số vấn đề dưới đây:
Chỉ sử dụng 1 loại thuốc nhất định và không phối trộn nhiều loại.
Sử dụng thuốc theo liều lượng, liệu trình trong thời gian nhất định. 
Khi gà mắc bệnh bà con cần cầm máu bằng cách sử dụng vitamin K kịp thời kết hợp các loại vitamin tổng hợp để gà tăng sức đề kháng và bệnh mau khỏi. Đối với gà mắc bệnh cần cách ly ở khu vực riêng, tránh lây nhiễm với đàn gà đang khỏe mạnh. 
5.2 Sử dụng phương pháp dân gian
Đối với những người chăn nuôi không muốn lạm dụng thuốc thú y thì bà con có thể tham khảo cách chữa trị dân gian. Bà con cần tìm lá mơ rừng. Mang lá mơ đi rửa sạch và đâm cùng với một ít muối hột chắt lấy nước. Tuỳ vào thể trọng, cân nặng của gà để cho uống từ 1ml và phần xác lá mơ mang đi trộn với thức ăn để cho gà ăn. 
